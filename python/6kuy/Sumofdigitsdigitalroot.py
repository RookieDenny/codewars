# 數字根是一個數字中所有數字的遞歸和。
# 給定n，取 的數字之和n。如果該值超過一位，則繼續以這種方式減少，直到產生一位數。輸入將是一個非負整數。
# 例子
#     16  -->  1 + 6 = 7
#    942  -->  9 + 4 + 2 = 15  -->  1 + 5 = 6
# 132189  -->  1 + 3 + 2 + 1 + 8 + 9 = 24  -->  2 + 4 = 6
# 493193  -->  4 + 9 + 3 + 1 + 9 + 3 = 29  -->  2 + 9 = 11  -->  1 + 1 = 2



def digital_root(n):
    while n >= 10:
        n = sum([int(x)for x in str(n)])
    return n


